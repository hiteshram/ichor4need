<p>Have you at anytime witnessed a relative of yours or a close friend searching frantically for a blood donor, when blood banks say out of stock, the donors in mind are out of reach and the time keeps ticking? Have you witnessed loss of life for the only reason that a donor was not available at the most needed hour?Is it something that we as a society can do nothing to prevent? This thought laid our foundation.

"Ichor4Need" is an organization that brings voluntary blood donors and those in need of blood on to a common platform.  Through this website, we seek donors who are willing to donate blood, as well as provide the timeliest support to those in frantic need of it. 

Started in the year 2014, on 14th may, in a small room with just 100 voluntary donors but with the zeal to serve our society, to inspire and spread the word, we dream to fulfil 100% blood need in India.

Our mission is to fulfil every blood request in the country with a promising web portal and motivated individuals who are willing to donate blood.

Our vision is to be The hope of every Indian in search of a voluntary blood donor

